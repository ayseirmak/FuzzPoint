#include <stdio.h>

int main() {
    // Initialize num1 and num2 with typical floating-point values
    double num1 = 10.0;  
    double num2 = 2.0;   
    double result = num1 / num2;

    // Print the result of the division
    printf("Division Result = %.2lf\n", result);

    // Return 0 to indicate the program executed successfully
    return 0;
}